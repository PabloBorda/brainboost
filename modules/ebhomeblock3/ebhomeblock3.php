class EBHomeBlock3 extends Module
{
	public function __construct()
	{
		$this->name = 'ebhomeblock3';
		$this->tab = 'front_office_features';
		$this->version = 2.0;
		$this->author = 'Elation Base';
		$this->need_instance = 0;

		$this->bootstrap = true;
		parent::__construct();

		$this->displayName = $this->l('Homepage Promo block #3');
		$this->description = $this->l('Displays parallax scrolling block in the homepage.');
	}

	public function install()
	{
		$languages = Language::getLanguages(false);
		$values = array();
		$values['EBHOMEBLOCK3_OPTION'] = '0';
		$values['EBHOMEBLOCK3_HEIGHT'] = '0';
		$values['EBHOMEBLOCK3_LINK'] = '0';

		foreach ($languages as $lang)
		{
			$values['EBHOMEBLOCK3_IMG'][$lang['id_lang']] = '';
			$values['EBHOMEBLOCK3_BG'][$lang['id_lang']] = '#444';
			$values['EBHOMEBLOCK3_DESC'][$lang['id_lang']] = '';
			$values['EBHOMEBLOCK3_TXT'][$lang['id_lang']] = 'Read More';
			$values['EBHOMEBLOCK3_URL'][$lang['id_lang']] = 'http://elationbase.com/';
		}

		Configuration::updateValue('EBHOMEBLOCK3_OPTION', $values['EBHOMEBLOCK3_OPTION']);
		Configuration::updateValue('EBHOMEBLOCK3_IMG', $values['EBHOMEBLOCK3_IMG']);
		Configuration::updateValue('EBHOMEBLOCK3_BG', $values['EBHOMEBLOCK3_BG']);
		Configuration::updateValue('EBHOMEBLOCK3_DESC', $values['EBHOMEBLOCK3_DESC']);
		Configuration::updateValue('EBHOMEBLOCK3_TXT', $values['EBHOMEBLOCK3_TXT']);
		Configuration::updateValue('EBHOMEBLOCK3_URL', $values['EBHOMEBLOCK3_URL']);

		return parent::install() && $this->registerHook('displayHome') && $this->registerHook('displayHeader');
	}

	public function uninstall()
	{
		Configuration::deleteByName('EBHOMEBLOCK3_HEIGHT');
		Configuration::deleteByName('EBHOMEBLOCK3_OPTION');
		Configuration::deleteByName('EBHOMEBLOCK3_LINK');
		Configuration::deleteByName('EBHOMEBLOCK3_IMG');
		Configuration::deleteByName('EBHOMEBLOCK3_BG');
		Configuration::deleteByName('EBHOMEBLOCK3_DESC');
		Configuration::deleteByName('EBHOMEBLOCK3_TXT');
		Configuration::deleteByName('EBHOMEBLOCK3_URL');
		return parent::uninstall();
	}

	public function hookDisplayHome()
	{
		if (!$this->isCached('ebhomeblock3.tpl', $this->getCacheId()))
		{
			$imgname = Configuration::get('EBHOMEBLOCK3_IMG', $this->context->language->id);

			if ($imgname && file_exists(_PS_MODULE_DIR_.$this->name.DIRECTORY_SEPARATOR.'img'.DIRECTORY_SEPARATOR.$imgname))
				$this->smarty->assign('banner_img', $this->context->link->protocol_content.Tools::getMediaServer($imgname).$this->_path.'img/'.$imgname);

			$this->smarty->assign(array(
				'background_height' => Configuration::get('EBHOMEBLOCK3_HEIGHT'),
				'background_option' => Configuration::get('EBHOMEBLOCK3_OPTION'),
				'background_link' => Configuration::get('EBHOMEBLOCK3_LINK'),
				'background_color' => Configuration::get('EBHOMEBLOCK3_BG', $this->context->language->id),
				'banner_desc' => Configuration::get('EBHOMEBLOCK3_DESC', $this->context->language->id),
				'banner_txt' => Configuration::get('EBHOMEBLOCK3_TXT', $this->context->language->id),
				'banner_url' => Configuration::get('EBHOMEBLOCK3_URL', $this->context->language->id)
			));
		}

		return $this->display(__FILE__, 'ebhomeblock3.tpl', $this->getCacheId());
	}

	public function postProcess()
	{
		if (Tools::isSubmit('submitStoreConf'))
		{
			$languages = Language::getLanguages(false);
			$values = array();
			$update_images_values = false;

			$values['EBHOMEBLOCK3_HEIGHT'] = Tools::getValue('EBHOMEBLOCK3_HEIGHT');
			$values['EBHOMEBLOCK3_OPTION'] = Tools::getValue('EBHOMEBLOCK3_OPTION');
			$values['EBHOMEBLOCK3_LINK'] = Tools::getValue('EBHOMEBLOCK3_LINK');
			
			foreach ($languages as $lang)
			{
				if (isset($_FILES['EBHOMEBLOCK3_IMG_'.$lang['id_lang']])
					&& isset($_FILES['EBHOMEBLOCK3_IMG_'.$lang['id_lang']]['tmp_name'])
					&& !empty($_FILES['EBHOMEBLOCK3_IMG_'.$lang['id_lang']]['tmp_name']))
				{
					if ($error = ImageManager::validateUpload($_FILES['EBHOMEBLOCK3_IMG_'.$lang['id_lang']], 4000000))
						return $this->displayError($this->l('Invalid image'));
					else
					{
						$ext = Tools::substr($_FILES['EBHOMEBLOCK3_IMG_'.$lang['id_lang']]['name'], strrpos($_FILES['EBHOMEBLOCK3_IMG_'.$lang['id_lang']]['name'], '.') + 1);
						$file_name = md5($_FILES['EBHOMEBLOCK3_IMG_'.$lang['id_lang']]['name']).'.'.$ext;

						if (!move_uploaded_file($_FILES['EBHOMEBLOCK3_IMG_'.$lang['id_lang']]['tmp_name'], dirname(__FILE__).DIRECTORY_SEPARATOR.'img'.DIRECTORY_SEPARATOR.$file_name))
							return $this->displayError($this->l('An error occurred while attempting to upload the file.'));
						else
						{
							if (Configuration::hasContext('EBHOMEBLOCK3_IMG', $lang['id_lang'], Shop::getContext())
								&& Configuration::get('EBHOMEBLOCK3_IMG', $lang['id_lang']) != $file_name)
								@unlink(dirname(__FILE__).DIRECTORY_SEPARATOR.'img'.DIRECTORY_SEPARATOR.Configuration::get('EBHOMEBLOCK3_IMG', $lang['id_lang']));

							$values['EBHOMEBLOCK3_IMG'][$lang['id_lang']] = $file_name;
						}
					}

					$update_images_values = true;
				}
				$values['EBHOMEBLOCK3_BG'][$lang['id_lang']] = Tools::getValue('EBHOMEBLOCK3_BG_'.$lang['id_lang']);
				$values['EBHOMEBLOCK3_DESC'][$lang['id_lang']] = Tools::getValue('EBHOMEBLOCK3_DESC_'.$lang['id_lang']);
				$values['EBHOMEBLOCK3_TXT'][$lang['id_lang']] = Tools::getValue('EBHOMEBLOCK3_TXT_'.$lang['id_lang']);
				$values['EBHOMEBLOCK3_URL'][$lang['id_lang']] = Tools::getValue('EBHOMEBLOCK3_URL_'.$lang['id_lang']);
			}

			if ($update_images_values)
				Configuration::updateValue('EBHOMEBLOCK3_IMG', $values['EBHOMEBLOCK3_IMG']);

			Configuration::updateValue('EBHOMEBLOCK3_HEIGHT', $values['EBHOMEBLOCK3_HEIGHT']);
			Configuration::updateValue('EBHOMEBLOCK3_OPTION', $values['EBHOMEBLOCK3_OPTION']);
			Configuration::updateValue('EBHOMEBLOCK3_LINK', $values['EBHOMEBLOCK3_LINK']);
			Configuration::updateValue('EBHOMEBLOCK3_BG', $values['EBHOMEBLOCK3_BG']);
			Configuration::updateValue('EBHOMEBLOCK3_DESC', $values['EBHOMEBLOCK3_DESC']);
			Configuration::updateValue('EBHOMEBLOCK3_TXT', $values['EBHOMEBLOCK3_TXT']);
			Configuration::updateValue('EBHOMEBLOCK3_URL', $values['EBHOMEBLOCK3_URL']);

			$this->_clearCache('ebhomeblock3.tpl');
			return $this->displayConfirmation($this->l('The settings have been updated.'));
		}
		return '';
	}

	public function getContent()
	{
		return $this->postProcess().$this->renderForm();
	}
	
