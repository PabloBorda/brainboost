	public function getConfigFieldsValues()
	{
		$languages = Language::getLanguages(false);
		$fields = array();

		$fields['EBHOMEBLOCK3_HEIGHT'] = Tools::getValue('EBHOMEBLOCK3_HEIGHT', Configuration::get('EBHOMEBLOCK3_HEIGHT'));
		$fields['EBHOMEBLOCK3_OPTION'] = Tools::getValue('EBHOMEBLOCK3_OPTION', Configuration::get('EBHOMEBLOCK3_OPTION'));
		$fields['EBHOMEBLOCK3_LINK'] = Tools::getValue('EBHOMEBLOCK3_LINK', Configuration::get('EBHOMEBLOCK3_LINK'));
		
		foreach ($languages as $lang)
		{
			$fields['EBHOMEBLOCK3_IMG'][$lang['id_lang']] = Tools::getValue('EBHOMEBLOCK3_IMG_'.$lang['id_lang'], Configuration::get('EBHOMEBLOCK3_IMG', $lang['id_lang']));
			$fields['EBHOMEBLOCK3_BG'][$lang['id_lang']] = Tools::getValue('EBHOMEBLOCK3_BG_'.$lang['id_lang'], Configuration::get('EBHOMEBLOCK3_BG', $lang['id_lang']));
			$fields['EBHOMEBLOCK3_DESC'][$lang['id_lang']] = Tools::getValue('EBHOMEBLOCK3_DESC_'.(int)$lang['id_lang'], Configuration::get('EBHOMEBLOCK3_DESC', $lang['id_lang']));
			$fields['EBHOMEBLOCK3_TXT'][$lang['id_lang']] = Tools::getValue('EBHOMEBLOCK3_TXT'.(int)$lang['id_lang'], Configuration::get('EBHOMEBLOCK3_TXT', $lang['id_lang']));
			$fields['EBHOMEBLOCK3_URL'][$lang['id_lang']] = Tools::getValue('EBHOMEBLOCK3_URL'.(int)$lang['id_lang'], Configuration::get('EBHOMEBLOCK3_URL', $lang['id_lang']));
		}

		return $fields;
	}
